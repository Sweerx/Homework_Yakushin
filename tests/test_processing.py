import pytest

from src.processing import filter_by_state, sort_by_date


def test_filter_of_state_executed(list_of_dist_filter_of_state, list_of_dist_filter_of_state_result_state_executed):
    assert filter_by_state(list_of_dist_filter_of_state) == list_of_dist_filter_of_state_result_state_executed

def test_filter_of_state_canceled(list_of_dist_filter_of_state, list_of_dist_filter_of_state_result_state_canceled):
    assert filter_by_state(list_of_dist_filter_of_state, 'CANCELED') == list_of_dist_filter_of_state_result_state_canceled

